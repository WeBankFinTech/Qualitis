package com.webank.wedatasphere.qualitis.metadata.request;

/**
 * @author howeye
 */
public class GetDbByUserAndClusterRequest {
    private String loginUser;
    private Integer startIndex;
    private Integer pageSize;
    private String clusterName;

    public GetDbByUserAndClusterRequest() {
    }

    public GetDbByUserAndClusterRequest(String loginUser, Integer startIndex, Integer pageSize, String clusterName) {
        this.loginUser = loginUser;
        this.startIndex = startIndex;
        this.pageSize = pageSize;
        this.clusterName = clusterName;
    }

    public String getLoginUser() {
        return loginUser;
    }

    public void setLoginUser(String loginUser) {
        this.loginUser = loginUser;
    }

    public String getClusterName() {
        return clusterName;
    }

    public void setClusterName(String clusterName) {
        this.clusterName = clusterName;
    }

    public Integer getStartIndex() {
        return startIndex;
    }

    public void setStartIndex(Integer startIndex) {
        this.startIndex = startIndex;
    }

    public Integer getPageSize() {
        return pageSize;
    }

    public void setPageSize(Integer pageSize) {
        this.pageSize = pageSize;
    }
}
